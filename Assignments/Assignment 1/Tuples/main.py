## 9. Tuples

# 1. Create a tuple containing three numbers
numbers = (10, 20, 30)  # Defines a tuple named `numbers` with three elements: 10, 20, and 30

# 2. Print the first and last elements of the tuple
print("First element:", numbers[0])  # Accesses and prints the first element of the tuple using index 0
print("Last element:", numbers[-1])  # Accesses and prints the last element of the tuple using index -1

