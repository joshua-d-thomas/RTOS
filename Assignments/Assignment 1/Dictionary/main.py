## 8. Dictionary

# 1. Create a dictionary with keys "name", "age", and "grade"
student_info = {  # Defines a dictionary named `student_info`
    "name": "Joshua Thomas",  # The key "name" is assigned the value "John Doe"
    "age": 25,           # The key "age" is assigned the value 20
    "grade": "A"         # The key "grade" is assigned the value "A"
}

# 2. Print the dictionary to the console
print(student_info)  # Outputs the entire dictionary