## 6. While Loop

# 1. Initialize a variable for the loop
num = 1  # Sets the starting number to 1

# 2. Use a while loop to print numbers from 1 to 10
while num <= 10:  # Continues the loop as long as `num` is less than or equal to 10
    
    print(num)  # Prints the current value of `num`
    
    num += 1  # Increments `num` by 1 after each iteration to move toward the loop's end condition