## 7. Lists and For Loop

# 1. Create a list of five fruits
fruits = ["Apple", "Banana", "Blueberry", "Raspberry", "Strawberry"]  # A list of fruit names is stored in the variable `fruits`

# 2. Use a for loop to print each fruit from the list
for fruit in fruits:  # Iterates over each item in the `fruits` list, assigning the current fruit to the variable `fruit` in each loop iteration
    
    print(fruit)  # Prints the current fruit to the console
