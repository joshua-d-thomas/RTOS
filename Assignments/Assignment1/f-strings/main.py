## 3. f-strings

# 1. Define variables for a name and a score
name = "Joshua"  # Assigns the string "Alice" to the variable `name`
score = 95  # Assigns the integer value 95 to the variable `score`

# 2. Output the message using an f-string
# f-strings allow you to embed variables directly within a string by prefixing the string with `f` and enclosing variables in curly braces
print(f"Congratulations {name}! You scored {score} points!")  # Prints the formatted message with the `name` and `score` variables embedded
